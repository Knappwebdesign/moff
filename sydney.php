                  <h1>Hello, my name is Sydney.</h1>

                  <p>Sydney was born on May 6, 1989.  Our first child!  We could not have been happier—we had a beautiful baby daughter.  She started talking at 9 months and walking two months later.  Other than wearing hearing aids at age 4 ½, Sydney was just like every other kid her age, although she was definitely not a tomboy.  Barbie pink was her color and still is her favorite color.  She enjoyed playing with dolls, dressing in nice clothes, and looking pretty.  She was clearly a future mother.  When she was in first or second grade, we noticed some learning disabilities, but nothing significant.  However, starting in the third grade, we noticed she was having difficulties keeping up with her academics and needed to move to a special education classroom.  We also noticed that her vision was deteriorating and she was diagnosed with retinal degeneration.</p>

