                  <p>When Sydney was 18 she moved to a group home.  This was an extremely difficult decision for us, but it was the right decision.  Her care was too significant for us to handle at home.  Safety and finding dependable and responsible caretakers were obstacles to great for us to overcome.  She thrived in the group for several years. She always loved being surrounded by people and activity. She was after all our “social “girl.  This past January we brought Sydney home. Her disease had progressed to the point that the group home could no longer handle her medical care.  This time we were able to obtain 24 hour nursing care in our home.</p>

                  <p>Sydney has not been able to walk for about five years.  A gastrostomy tube was placed approximately 7 years ago.  Initially she needed it only for fluids but now needs it to receive all her nutrition and fluids. She has not been verbal for a few years which has been very difficult. Sydney had the gift of gab and was forever chattering on about things. We miss her voice.  She is now home and has 24/7 nursing care.  She is comfortable and we believe she knows she is home.</p>

                  <p>Needless to say, the diagnosis was devastating, but we were glad we knew none the less.  We knew what we were facing and now just had to deal with it.  Sydney went through many of the typical phases of a child, teenager, and high school student, but as the disease progressed, normal became something different than what we expected.</p>

